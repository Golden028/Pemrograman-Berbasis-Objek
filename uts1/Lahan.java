package uts1;

public class Lahan {
    private double ukuran;
    private String jenisTanah;

    public Lahan(double ukuran, String jenisTanah) {
        this.ukuran = ukuran;
        this.jenisTanah = jenisTanah;
    }

    public double getUkuran() {
        return ukuran;
    }

    public String getJenisTanah() {
        return jenisTanah;
    }
}
